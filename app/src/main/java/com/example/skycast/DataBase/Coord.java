package com.example.skycast.DataBase;

public class Coord {
    private double lon;
    private double lat;

    public double getLon() { return lon; }
    public double getLat() { return lat; }

    public void setLon(double lon) {
        this.lon = lon;
    }

    public void setLat(double lat) {
        this.lat = lat;
    }
}